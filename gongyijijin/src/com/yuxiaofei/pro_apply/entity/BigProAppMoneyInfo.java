package com.yuxiaofei.pro_apply.entity;

public class BigProAppMoneyInfo {
	private Integer pro_id;
	private Integer user_id;
	private String money_app_num;
	private String money_app_reason;
	public Integer getPro_id() {
		return pro_id;
	}
	public void setPro_id(Integer pro_id) {
		this.pro_id = pro_id;
	}
	public Integer getUser_id() {
		return user_id;
	}
	public void setUser_id(Integer user_id) {
		this.user_id = user_id;
	}
	public String getMoney_app_num() {
		return money_app_num;
	}
	public void setMoney_app_num(String money_app_num) {
		this.money_app_num = money_app_num;
	}
	public String getMoney_app_reason() {
		return money_app_reason;
	}
	public void setMoney_app_reason(String money_app_reason) {
		this.money_app_reason = money_app_reason;
	}
}
